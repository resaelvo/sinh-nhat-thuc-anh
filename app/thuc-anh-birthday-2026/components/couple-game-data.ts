export function pickRandom<T>(arr: readonly T[], avoid?: T): T {
  if (arr.length === 0) throw new Error("empty");
  let v: T;
  let guard = 0;
  do {
    v = arr[Math.floor(Math.random() * arr.length)]!;
    guard++;
  } while (avoid !== undefined && arr.length > 1 && v === avoid && guard < 24);
  return v;
}

/** Lần quay sau không trùng ô với lần trước (khi có ≥ 2 ô). */
export function pickWheelIndexExcluding(
  length: number,
  exclude: number | null,
): number {
  if (length <= 0) return 0;
  if (length === 1) return 0;
  let i = Math.floor(Math.random() * length);
  if (exclude === null) return i;
  let guard = 0;
  while (i === exclude && guard < 64) {
    i = Math.floor(Math.random() * length);
    guard++;
  }
  if (i === exclude) i = (exclude + 1) % length;
  return i;
}

/** 5 cặp this-or-that xáo trộn cho trò siêu tốc. */
export function sampleSpeedTotRound(): [string, string][] {
  const a = [...SPEED_TOT_PAIRS];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const t = a[i]!;
    a[i] = a[j]!;
    a[j] = t;
  }
  return a.slice(0, 5);
}
